#ifndef RTOS_MASTERMIND_PLAYER_H
#define RTOS_MASTERMIND_PLAYER_H

#include <vector>
#include <cstdlib>
#include <iostream>

using namespace std;
using nat = unsigned;


class Player {

private:
    nat colors;
    nat positions;

public:
    Player() = default;
    Player(nat colors, nat positions);

    nat* pickFirstSolution();

    // function used to analyze the context before launching the threads to find
    // the new solution proposition
    vector<nat*> analyzeContext(vector<nat*> previousPropositions);

    // picks randomly one of the solutions generated by a processor
    nat* pickSolution();

    bool proposalIsDifferent(const nat* proposal, const nat* reference);

    bool proposalRespectsKnowledge(const nat* proposal, const nat* reference, const nat* referenceScore);

    bool plausibleSolution(const vector<nat*> *previousGuesses, const vector<nat*> *previousEvaluations, nat* solution);
};


#endif //RTOS_MASTERMIND_PLAYER_H
